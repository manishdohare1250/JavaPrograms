// Relational operator
public class _33_Relational_operator {
    public static void main(String[] args) {
        int a=10;
        int b=5;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        
       
    // output---
    //     false
    //     true
    //     true
    //     false
    //     true
    //     false

    }
}
